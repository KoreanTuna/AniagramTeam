export function PixelIcon({ name, size = 16, color = "currentColor", accent }: Props) {
  const rows = ICONS[name];
  const a = accent ?? color;
  const rects: JSX.Element[] = [];
  for (let r = 0; r < rows.length; r++) {
    const row = rows[r];
    for (let c = 0; c < row.length; c++) {
      const ch = row[c];
      if (ch === ".") continue;
      const fill = ch === "@" ? a : color;
      rects.push(
        <rect
          key={`${r}-${c}`}
          x={c}
          y={r}
          width={1}
          height={1}
          fill={fill}
          shapeRendering="crispEdges"
        />
      );
    }
  }
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 9 9"
      style={{ display: "inline-block", verticalAlign: "-0.1em", imageRendering: "pixelated" }}
    >
      {rects}
    </svg>
  );
}

